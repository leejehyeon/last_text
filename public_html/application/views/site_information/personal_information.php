<p class="person_information_title">1. 개인정보의 처리 목적</p>
<p class="person_information_sub">- ‘MSC 튜터튜티‘에서 처리한 개인정보는 다음의 목적이외의 용도로는 사용되지 않으며 이용 목적이 변경될 시에는 사전 동의를 구할</br> 예정입니다.</p>

	<p class="person_information_contents">가. 서비스 제공 : ‘MSC 튜터튜티’의 공지사항, 교육콘텐츠 제공 등의 서비스 제공에 관련한 목적으로 개인정보를 처리합니다.</p>
	<p class="person_information_contents_last">나. 회원가입 및 관리 : ‘MSC 튜터튜티‘에서 제공하는 튜터링 서비스 이용에 따른 본인확인, 개인 식별, 공지사항 전달 등의 목적으로 </br>개인정보를 처리합니다.</p>

<p class="person_information_title">2. 개인정보의 제3자 제공</p>
<p class="person_information_sub">- ‘MSC 튜터튜티’는 원칙적으로 이용자의 개인정보를 개인정보의 처리 목)에서 명시한 범위 내에서 처리하며, 이용자의 사전 동의 없이는</br> 본래의 범위를 초과하여 처리하거나 제3자에게 제공하지 않습니다. 단, 다음의 경우에는 예외적으로 개인정보를 처리할 수 있습니다.</p>

	<p class="person_information_contents">가. 이용자가 사전에 제3자 제공 및 공개에 동의한 경우</p>
	<p class="person_information_contents">나. 법령 등에 의해 제공이 요구되는 경우</p>
	<p class="person_information_contents">다. 서비스의 제공에 관한 계약의 이행을 위하여 필요한 개인정보로서 경제적/기술적인 사유로 통상의 동의를 받는 것이 현저히</br>곤란한 경우</p>
	<p class="person_information_contents_last">라. 개인을 식별하기에 특정할 수 없는 상태로 가공하여 이용하는 경우</p>

<p class="person_information_title">3. 정보주체의 권리·의무 및 그 행사방법</p>
<p class="person_information_sub">- 개인정보 열람 요구 : ‘MSC 튜터튜티‘에서 보유하고 있는 개인정보파일은 「개인정보보호법」 제35조(개인정보의 열람)에 따라 열람을</br>요구할 수 있으며,
     개인정보 열람 요구 시 법 제35조 5항에 의하여 열람을 제한할 수 있습니다.</p>
	
	<p class="person_information_sub">- 법률에 따라 열람이 금지되거나 제한되는 경우</p>
	<p class="person_information_contents">가. 다른 사람의 생명·신체를 해할 우려가 있거나 다른 사람의 재산과 그 밖의 이익을 부당하게 침해할 우려가 있는 경우</p>
	<p class="person_information_contents">나. 공공기관이 다음 각 목의 어느 하나에 해당하는 업무를 수행할 때 중대한 지장을 초래하는 경우</p>
		<p class="person_information_contents_list">ㄱ. 조세의 부과·징수 또는 환급에 관한 업무</p>
		<p class="person_information_contents_list">ㄴ. 「초·중등교육법」 및 「고등교육법」에 따른 각급 학교, 「평생교육법」에 따른 평생교육시설, 그 밖의 다른 법률에 따라 설치 된</br> 고등교육기관에서의 성적 평가 또는 입학자 선발에 관한 업무</p>
		<p class="person_information_contents_list">ㄷ. 학력·기능 및 채용에 관한 시험, 자격 심사에 관한 업무</p>
		<p class="person_information_contents_list">ㄹ. 보상금·급부금 산정 등에 대하여 진행 중인 평가 또는 판단에 관한 업무</p>
		<p class="person_information_contents_list_last">ㅁ. 다른 법률에 따라 진행 중인 감사 및 조사에 관한 업무</p>

<p class="person_information_sub">- 개인정보 정정·삭제 요구 : ‘MSC 튜터튜티‘에서 보유하고 있는 개인정보파일은 「개인정보보호법」 제36조(개인정보의 정정·삭제)에 따라 </br>정정·삭제를 
     요구할 수 있지만, 다른 법령에서 그 개인정보가 수집 대상으로 명시되어 있는 경우에는 그 삭제를 요구할 수 없습니다.</p>

<p class="person_information_sub">- 개인정보 처리정지 요구 : ‘MSC 튜터튜티‘에서 보유하고 있는 개인정보파일은 「개인정보보호법」 제37조(개인정보의 처리정지 등)에 따라</br> 처리정지를
     요구할 수 있으며, 개인정보처리정지 요구 시 법 제37조 2항에 의하여 처리정지 요구를 거절할 수 있습니다.</p>

	<p class="person_information_contents">가. 법률에 특별한 규정이 있거나 법령상 의무를 준수하기 위하여 불가피한 경우</p>
	<p class="person_information_contents">나. 다른 사람의 생명, 신체를 해할 우려가 있거나 다른 사람의 재산과 그 밖의 이익을 부당하게 침해할 우려가 있는 경우</p>
	<p class="person_information_contents">다. 공공기관이 개인정보를 처리하지 아니하면 다른 법률에서 정하는 소관 업무를 수행할 수 없는 경우</p>
	<p class="person_information_contents_last">라. 개인정보를 처리하지 아니하면 정보주체와 약정한 서비스를 제공하지 못하는 등 계약의 이행이 곤란한 경우로서 정보주체가 </br>그 계약의 해지 의사를 명확하게 밝히지 아니한 경우</p>

<p class="person_information_title">4. 개인정보의 파기</p>
<p class="person_information_sub">- ‘MSC 튜터튜티‘는 원칙적으로 개인정보 처리목적이 달성된 경우에는 지체없이 해당 개인정보를 파기합니다. 하지만 다른 법률에 따라</br>보존하여야하는 경우에는 예외로, 파기의 절차, 기한 및 방법은 다음과 같습니다.</p>
<p class="person_information_contents">가. 파기절차 : 이용자가 입력한 정보는 목적 달성 후 내부 방침 및 관련 법령에 따라 일정기간 저장된 후 혹은 즉시 파기됩니다.</p>
<p class="person_information_contents">나. 파기기한 : 이용자의 개인정보는 개인정보의 보유기간이 경과된 경우 보유기간의 종료일로부터 5일 이내에, 개인정보의</br> 처리 목적 달성 등 그 개인
    정보가 불필요하게 되었을 때에는 개인그정보의 처리가 불필요한 것으로 인정되는 날로부터 5일 이내에 </br>그 개인정보를 파기합니다.</p>
<p class="person_information_contents_last">다. 파기방법 : 종이에 출력된 개인정보는 분쇄기로 분쇄하거나 소각을 통하여 파기하며 전자적 파일 형태의 정보는 기록을 재생할 수 없는 </br>기술적
    방법을 사용하여 파기합니다.</p>

